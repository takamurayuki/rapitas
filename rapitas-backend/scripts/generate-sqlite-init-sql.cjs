#!/usr/bin/env node
const { execFileSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const backendDir = path.resolve(__dirname, '..');
const schemaDir = path.join(backendDir, 'prisma', 'schema.desktop');
const outputFile = path.join(backendDir, 'src', 'generated', 'sqlite-init-sql.ts');

function escapeTemplateLiteral(value) {
  return value.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

try {
  require('./generate-sqlite-prisma-schema.cjs');

  const sql = execFileSync(
    'bunx',
    ['prisma', 'migrate', 'diff', '--from-empty', '--to-schema-datamodel', schemaDir, '--script'],
    {
      cwd: backendDir,
      encoding: 'utf8',
      env: {
        ...process.env,
        DATABASE_URL: process.env.DATABASE_URL || 'file:./rapitas-desktop.db',
      },
    },
  );

  fs.mkdirSync(path.dirname(outputFile), { recursive: true });
  fs.writeFileSync(
    outputFile,
    [
      '// Generated by scripts/generate-sqlite-init-sql.cjs. Do not edit manually.',
      'export const SQLITE_INIT_SQL = String.raw`',
      escapeTemplateLiteral(sql.trim()),
      '`;',
      '',
    ].join('\n'),
  );

  console.log(`Generated SQLite init SQL: ${outputFile}`);
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
}
